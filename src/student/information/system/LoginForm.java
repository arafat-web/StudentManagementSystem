    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        // TODO add your handling code here:

        if (usernameInput.getText().equalsIgnoreCase("Username") || passwordInput.getText().equalsIgnoreCase("Password") || usernameInput.getText().equalsIgnoreCase("") || passwordInput.getText().equalsIgnoreCase("")) {
            PopUp popUp = new PopUp();
            popUp.setVisible(true);
            popUp.setHeader.setText("Blank Input!");
            popUp.setMsg.setText("Please enter your input.");
            usernameInput.setText("");
            passwordInput.setText("");
        } else {

            String sql = "select * from logininfo where name = ? and pass = ?";

            try {
                ps = conn.prepareStatement(sql);
                ps.setString(1, usernameInput.getText());
                ps.setString(2, passwordInput.getText());
                rs = ps.executeQuery();
                if (rs.next()) {
                    rs.close();
                    ps.close();

                    this.dispose();
                    WelcomeScreen welcomeScreen = new WelcomeScreen();
                    welcomeScreen.setVisible(true);
                    welcomeScreen.setUploading();

                } else {
                    PopUp popUp = new PopUp();
                    popUp.setVisible(true);
                    popUp.setHeader.setText("Wrong Input!");
                    popUp.setMsg.setText("Please enter correct input.");
                    usernameInput.setText("");
                    passwordInput.setText("");
                }

            } catch (Exception ex) {
                System.out.println(ex);

            } finally {
                try {

                    rs.close();
                    ps.close();
                } catch (Exception ex) {
                    System.out.println(ex);

                }
            }
        }
    }//GEN-LAST:event_loginButtonMouseClicked

